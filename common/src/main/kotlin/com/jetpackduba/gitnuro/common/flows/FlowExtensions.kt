    flow2: Flow<T2>,
    flow3: Flow<T3>,
    transform: suspend (a: T1, b: T2, c: T3) -> R
): Flow<R> = combine(this@combine, flow2, flow3) {
    transform(it[0] as T1, it[1] as T2, it[2] as T3)
}